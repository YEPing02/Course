package data;

public class Addcoureur {
	private int idcou;
	private String nomcou;
	private String prenomcou;
	private String datenaissance;
	private String sang;
	private int idv;

	public Addcoureur() {
	}

	public int getIdcou() {
		return idcou;
	}

	public void setIdcou(int idcou) {
		this.idcou = idcou;
	}

	public String getNomcou() {
		return nomcou;
	}

	public void setNomcou(String nomcou) {
		this.nomcou = nomcou;
	}

	public String getPrenomcou() {
		return prenomcou;
	}

	public void setPrenomcou(String prenomcou) {
		this.prenomcou = prenomcou;
	}

	public String getDatenaissance() {
		return datenaissance;
	}

	public void setDatenaissance(String datenaissance) {
		this.datenaissance = datenaissance;
	}

	public String getSang() {
		return sang;
	}

	public void setSang(String sang) {
		this.sang = sang;
	}

	public int getIdv() {
		return idv;
	}

	public void setIdv(int idv) {
		this.idv = idv;
	}

}
